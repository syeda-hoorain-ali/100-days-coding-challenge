// Number
let age = 25; // number type
console.log(typeof age); // should output "number"

// String
let name = "John"; // string type
console.log(typeof name); // should output "string"

// Boolean
let isStudent = true; // boolean type
console.log(typeof isStudent); // should output "boolean"

// Null
let emptyValue = null; // null type
console.log(typeof emptyValue); // should output "object" (special case in JavaScript)

// Undefined
let notDefined; // undefined type
console.log(typeof notDefined); // should output "undefined"

// Object
let user = { name: "John", age: 25 }; // object type
console.log(typeof user); // should output "object"

